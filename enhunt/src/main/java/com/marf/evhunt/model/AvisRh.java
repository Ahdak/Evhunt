public class AvisRh {

	private int id;
	private int idCandidat;
	private String dateAvis;
	private String avis;
	private String rhName;

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the dateAvis
	 */
	public String getDateAvis() {
		return dateAvis;
	}

	/**
	 * @param dateAvis
	 *            the dateAvis to set
	 */
	public void setDateAvis(String dateAvis) {
		this.dateAvis = dateAvis;
	}

	/**
	 * @return the avis
	 */
	public String getAvis() {
		return avis;
	}

	/**
	 * @param avis
	 *            the avis to set
	 */
	public void setAvis(String avis) {
		this.avis = avis;
	}

	/**
	 * @return the idCandidat
	 */
	public int getIdCandidat() {
		return idCandidat;
	}

	/**
	 * @param idCandidat
	 *            the idCandidat to set
	 */
	public void setIdCandidat(int idCandidat) {
		this.idCandidat = idCandidat;
	}

	/**
	 * @return the rhName
	 */
	public String getRhName() {
		return rhName;
	}

	/**
	 * @param rhName
	 *            the rhName to set
	 */
	public void setRhName(String rhName) {
		this.rhName = rhName;
	}

}
